<script>
    const images = [
        { src: "/Drives/images/apple.png", caption: "Apple" },
        { src: "images/infosys.png", caption: "InfoSys" },
        { src: "images/google.jpg", caption: "Google" },
		{ src: "images/microsoft.jpg", caption: "Microsoft" },
		{ src: "images/tesla.jpg", caption: "Tesla" },
		{ src: "images/intel.jpg", caption: "Intel" },
    ];

    let currentIndex = 0;

    const imageElement = document.getElementById("carouselImage");
    const captionElement = document.getElementById("imageCaption");
    const prevBtn = document.getElementById("prevBtn");
    const nextBtn = document.getElementById("nextBtn");

    function updateImage() {
        imageElement.src = images[currentIndex].src;
        imageElement.alt = images[currentIndex].caption;
        captionElement.textContent = images[currentIndex].caption;
    }

    nextBtn.addEventListener("click", () => {
        currentIndex = (currentIndex + 1) % images.length;
        updateImage();
    });

    prevBtn.addEventListener("click", () => {
        currentIndex = (currentIndex - 1 + images.length) % images.length;
        updateImage();
    });

    updateImage();
</script>

